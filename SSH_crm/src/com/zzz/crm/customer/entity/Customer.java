package com.zzz.crm.customer.entity;

import java.util.List;
import java.util.Set;

import com.zzz.crm.dict.entity.Dict;
import com.zzz.crm.linkman.entity.Linkman;
import com.zzz.crm.visit.entity.Visit;

public class Customer {
	private Integer cid;
	private String custName;
	private String custSource;
	private String custPhone;
	private Set<Linkman> linkmans;
	private Set<Visit> customerVisitSet;
	private Dict dict;
	public Dict getDict() {
		return dict;
	}
	public void setDict(Dict dict) {
		this.dict = dict;
	}
	public Set<Visit> getCustomerVisitSet() {
		return customerVisitSet;
	}
	public void setCustomerVisitSet(Set<Visit> customerVisitSet) {
		this.customerVisitSet = customerVisitSet;
	}
	public Set<Linkman> getLinkmans() {
		return linkmans;
	}
	public void setLinkmans(Set<Linkman> linkmans) {
		this.linkmans = linkmans;
	}
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public String getCustName() {
		return custName;
	}
	public void setCustName(String custName) {
		this.custName = custName;
	}
	public String getCustSource() {
		return custSource;
	}
	public void setCustSource(String custSource) {
		this.custSource = custSource;
	}
	public String getCustPhone() {
		return custPhone;
	}
	public void setCustPhone(String custPhone) {
		this.custPhone = custPhone;
	}
}
